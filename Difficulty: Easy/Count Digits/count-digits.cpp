class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int cnt=0;
        int copy=N;
        while(N>0)
        {
            int num=N%10;
            if (num!=0 && copy%num==0 )
               cnt++;
            
            N=N/10;
        }
        return cnt;
    }
};

